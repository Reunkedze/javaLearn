package com.reunkedze.lesson4;

public class SwitchExample {

    public static void main(String[] args) {
        int value = 3;
        switch (value) {
            case 1:
                System.out.println("equal 1");
                break;
            case 2:
                System.out.println("equal 2");
                break;
            default:
                System.out.println("no value");
        }
    }
}
